package day1;

public class FloatArthimetic {
	public static void main (String [] args) {
		
		float num = 9.9f;
		float num1 = 1.1f;
		float num2 = num + num1;
		float num3 = num - num1;
		float num4 = num * num1;
		float num5 = num / num1;
		float num6 = num % num1;
		
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num4);
		System.out.println(num5);
		System.out.println(num6);
		
	}

}
